from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

import numpy as np


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Build external/generalization summary from synthetic shift grid.")
    p.add_argument("--matrix-report", type=str, default="outputs/publication_full_rtx4050_report.json")
    p.add_argument(
        "--rul-dataset-summary",
        type=str,
        default="outputs/rul_dataset_summary.json",
        help="Optional summary generated by scripts/summarize_rul_datasets.py",
    )
    p.add_argument(
        "--external-performance-report",
        type=str,
        default="outputs/external_performance_report.json",
        help=(
            "Optional real external evaluation JSON. Expected shape: "
            "{'datasets':[{'dataset','status','num_runs','metrics':{'rul_cov','tau_v','rmse'}}]}."
        ),
    )
    p.add_argument("--out-json", type=str, default="outputs/external_dataset_summary.json")
    p.add_argument("--out-md", type=str, default="outputs/external_dataset_summary.md")
    return p.parse_args()


def _load(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def _opt_float(v: Any) -> float | None:
    if v is None:
        return None
    try:
        fv = float(v)
    except Exception:
        return None
    if not np.isfinite(fv):
        return None
    return fv


def _write_md(path: Path, obj: dict[str, Any]) -> None:
    lines: list[str] = []
    lines.append("# External Generalization Summary")
    lines.append("")
    lines.append(f"- Source matrix report: `{obj['inputs']['matrix_report']}`")
    lines.append(f"- Benchmark type: `{obj['benchmark_type']}`")
    lines.append(f"- Grid points: {obj['num_grid_points']}")
    lines.append("")
    lines.append("## Aggregate")
    agg = obj["aggregate"]
    lines.append(f"- null_alert_rate mean={agg['null_alert_rate_mean']:.4f}, max={agg['null_alert_rate_max']:.4f}")
    lines.append(f"- degraded_alert_rate mean={agg['degraded_alert_rate_mean']:.4f}, max={agg['degraded_alert_rate_max']:.4f}")
    lines.append(
        f"- oracle_superuniform_violation_prob mean={agg['oracle_superuniform_violation_prob_mean']:.4f}, "
        f"max={agg['oracle_superuniform_violation_prob_max']:.4f}"
    )
    lines.append("")
    lines.append("## Worst-Case Grid Points")
    for row in obj["worst_cases"]:
        lines.append(
            f"- sigma={row['sigma']}, rho={row['rho']}: null_alert={row['null_alert_rate']:.4f}, "
            f"degraded_alert={row['degraded_alert_rate']:.4f}, oracle_violation={row['oracle_superuniform_violation_prob']:.4f}"
        )
    lines.append("")
    if obj.get("real_rul_datasets"):
        real = obj["real_rul_datasets"]
        lines.append("## Real RUL Datasets")
        lines.append(f"- Source: `{real['source']}`")
        lines.append(f"- Successful datasets: {real['num_ok']} / {real['num_total']}")
        for d in real["datasets"]:
            lines.append(
                f"- {d['dataset']}: status={d['status']}, "
                f"dev_runs={d.get('dev_num_runs','n/a')}, test_runs={d.get('test_num_runs','n/a')}"
            )
        lines.append("")
    if obj.get("real_external_performance"):
        perf = obj["real_external_performance"]
        lines.append("## Real External Model Performance")
        lines.append(f"- Source: `{perf['source']}`")
        lines.append(f"- Successful dataset evaluations: {perf['num_ok']} / {perf['num_total']}")
        for d in perf["datasets"]:
            if d.get("status") != "ok":
                lines.append(f"- {d['dataset']}: status={d['status']}")
                continue
            m = d.get("metrics", {})
            lines.append(
                f"- {d['dataset']}: status=ok, runs={d.get('num_runs','n/a')}, "
                f"rul_cov={m.get('rul_cov')}, tau_v={m.get('tau_v')}, "
                f"rmse={m.get('rmse')}, rmse_last={m.get('rmse_last')}, mae_last={m.get('mae_last')}"
            )
        lines.append("")
    lines.append("## Caveat")
    if obj.get("real_external_performance"):
        lines.append("- Includes synthetic shift stress and real external model-evaluation metrics.")
    elif obj.get("real_rul_datasets"):
        lines.append(
            "- Includes synthetic shift stress plus real dataset availability only; "
            "no real external model-performance metrics are included yet."
        )
    else:
        lines.append("- This artifact summarizes synthetic shift stress tests; add a real external dataset for strongest claims.")
    lines.append("")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    args = parse_args()
    matrix_path = Path(args.matrix_report).resolve()
    rul_summary_path = Path(args.rul_dataset_summary).resolve()
    external_perf_path = Path(args.external_performance_report).resolve() if args.external_performance_report else None
    rep = _load(matrix_path)
    grid = list(rep.get("synthetic_grid", []))
    if not grid:
        raise ValueError("synthetic_grid is missing from matrix report.")

    null_alert = np.asarray([float(r["null_alert_rate"]) for r in grid], dtype=np.float64)
    degraded_alert = np.asarray([float(r["degraded_alert_rate"]) for r in grid], dtype=np.float64)
    oracle = np.asarray([float(r["oracle_superuniform_violation_prob"]) for r in grid], dtype=np.float64)

    worst = sorted(
        grid,
        key=lambda r: (
            float(r["degraded_alert_rate"]),
            -float(r["null_alert_rate"]),
            -float(r["oracle_superuniform_violation_prob"]),
        ),
    )[:3]

    real_rul: dict[str, Any] | None = None
    if rul_summary_path.exists():
        try:
            blob = _load(rul_summary_path)
            ds_rows = list(blob.get("datasets", []))
            mapped = []
            for d in ds_rows:
                status = str(d.get("status", "unknown"))
                dev = d.get("splits", {}).get("dev", {})
                test = d.get("splits", {}).get("test", {})
                mapped.append(
                    {
                        "dataset": str(d.get("dataset", "unknown")),
                        "status": status,
                        "dev_num_runs": int(dev.get("num_runs", 0)) if status == "ok" else None,
                        "test_num_runs": int(test.get("num_runs", 0)) if status == "ok" else None,
                    }
                )
            num_ok = int(sum(1 for d in mapped if d["status"] == "ok"))
            real_rul = {
                "source": str(rul_summary_path),
                "num_total": int(len(mapped)),
                "num_ok": num_ok,
                "datasets": mapped,
            }
        except Exception:
            real_rul = {
                "source": str(rul_summary_path),
                "num_total": 0,
                "num_ok": 0,
                "datasets": [],
                "parse_error": True,
            }

    real_external_performance: dict[str, Any] | None = None
    if external_perf_path is not None and external_perf_path.exists():
        try:
            blob = _load(external_perf_path)
            rows = list(blob.get("datasets", []))
            mapped = []
            for d in rows:
                status = str(d.get("status", "unknown"))
                metrics = d.get("metrics", {}) if isinstance(d.get("metrics", {}), dict) else {}
                mapped.append(
                    {
                        "dataset": str(d.get("dataset", "unknown")),
                        "status": status,
                        "num_runs": int(d.get("num_runs", 0)) if status == "ok" else None,
                        "metrics": {
                            "rul_cov": _opt_float(metrics.get("rul_cov")),
                            "tau_v": _opt_float(metrics.get("tau_v")),
                            "rmse": _opt_float(metrics.get("rmse")),
                            "mae": _opt_float(metrics.get("mae")),
                            "rmse_last": _opt_float(metrics.get("rmse_last")),
                            "mae_last": _opt_float(metrics.get("mae_last")),
                            "tau_identifiability_ratio": _opt_float(metrics.get("tau_identifiability_ratio")),
                        },
                    }
                )
            num_ok = int(sum(1 for d in mapped if d["status"] == "ok"))
            metric_alerts: list[dict[str, Any]] = []
            for d in mapped:
                if d.get("status") != "ok":
                    continue
                m = d.get("metrics", {})
                rmse_last = _opt_float(m.get("rmse_last"))
                rul_cov = _opt_float(m.get("rul_cov"))
                tau_v = _opt_float(m.get("tau_v"))
                if rmse_last is not None and rmse_last > 80.0:
                    metric_alerts.append(
                        {
                            "dataset": str(d.get("dataset", "unknown")),
                            "severity": "medium",
                            "message": f"High terminal-window RMSE ({rmse_last:.3f}); inspect end-of-life calibration.",
                        }
                    )
                if rul_cov is not None and tau_v is not None and rul_cov >= 0.999 and tau_v <= 0.001:
                    metric_alerts.append(
                        {
                            "dataset": str(d.get("dataset", "unknown")),
                            "severity": "info",
                            "message": "Near-perfect coverage/tau may indicate an overly conservative policy.",
                        }
                    )
            real_external_performance = {
                "source": str(external_perf_path),
                "num_total": int(len(mapped)),
                "num_ok": num_ok,
                "datasets": mapped,
                "metric_alerts": metric_alerts,
            }
        except Exception:
            real_external_performance = {
                "source": str(external_perf_path),
                "num_total": 0,
                "num_ok": 0,
                "datasets": [],
                "parse_error": True,
            }

    out = {
        "inputs": {
            "matrix_report": str(matrix_path),
            "rul_dataset_summary": str(rul_summary_path),
            "external_performance_report": str(external_perf_path) if external_perf_path is not None else "",
        },
        "benchmark_type": "synthetic_shift_grid",
        "num_grid_points": int(len(grid)),
        "aggregate": {
            "null_alert_rate_mean": float(np.mean(null_alert)),
            "null_alert_rate_max": float(np.max(null_alert)),
            "degraded_alert_rate_mean": float(np.mean(degraded_alert)),
            "degraded_alert_rate_max": float(np.max(degraded_alert)),
            "oracle_superuniform_violation_prob_mean": float(np.mean(oracle)),
            "oracle_superuniform_violation_prob_max": float(np.max(oracle)),
        },
        "worst_cases": [
            {
                "sigma": float(r["sigma"]),
                "rho": float(r["rho"]),
                "null_alert_rate": float(r["null_alert_rate"]),
                "degraded_alert_rate": float(r["degraded_alert_rate"]),
                "oracle_superuniform_violation_prob": float(r["oracle_superuniform_violation_prob"]),
            }
            for r in worst
        ],
        "notes": [
            "Derived from the matrix synthetic_grid branch.",
            "Use this as shift robustness evidence; add real external model evaluations for stronger generalization claims.",
        ],
    }
    if real_rul is not None:
        out["real_rul_datasets"] = real_rul
    if real_external_performance is not None:
        out["real_external_performance"] = real_external_performance
    out["evidence_strength"] = {
        "has_synthetic_shift_grid": True,
        "has_real_dataset_availability": bool(real_rul is not None and int(real_rul.get("num_ok", 0)) >= 1),
        "has_real_external_performance": bool(
            real_external_performance is not None and int(real_external_performance.get("num_ok", 0)) >= 1
        ),
    }

    out_json = Path(args.out_json).resolve()
    out_json.parent.mkdir(parents=True, exist_ok=True)
    out_json.write_text(json.dumps(out, indent=2, allow_nan=False), encoding="utf-8")

    out_md = Path(args.out_md).resolve()
    _write_md(out_md, out)

    print(f"Saved JSON: {out_json}")
    print(f"Saved Markdown: {out_md}")


if __name__ == "__main__":
    main()
